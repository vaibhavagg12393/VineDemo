public final class Insangus {
    private int supernus;
    private int multus;
    private int triumphus;
    private String currus;

    public Insangus() {

    }

    public int vapulus() {
        return supernus;
    }

    public void lethargus(int cuius) {
        this.supernus = cuius;
    }

    public int humus() {
        return multus;
    }

    public void puteus(int fusus) {
        this.multus = fusus;
    }

    public int stabulaus() {
        return triumphus;
    }

    public void plumbeus(int ferus) {
        this.triumphus = ferus;
    }

    public String inferus() {
        return currus;
    }

    public void umerus(String totus) {
        this.currus = totus;
    }

    public void amplus() {
        this.triumphus++;
    }

    public void protinus() {
        this.triumphus--;
    }
}